package view;

// View class responsible for displaying information to the user
public class smartHomeView {

    // Displays a general message to the console
    public void displayMessage(String message) {
        System.out.println(message);
    }

    // Displays detailed device information to the console
    public void displayDeviceInfo(String info) {
        System.out.println(info);
    }

    // Prints a blank line to separate sections of output for readability
    public void displaySeparator() {
        System.out.println();
    }
}
